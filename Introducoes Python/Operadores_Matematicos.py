""" Operadores Matemáticos """

"""
    (+) ---> Adição
    (-) ---> Subtração
    (*) ---> Multiplicação
    (/) ---> Divisão
    (%) ---> Resto da Divisão
   (**) ---> Elevado Exponeciação
   
"""
print("Adição",2 + 2)               # Adição
print("Subtração",2 -2)             # Subtração
print("Multiplicação",2 * 2)        # Multiplicação
print("Divisão",12 / 5)             # Divisão
print("Resto a Divisão",12 % 5)     # Operadores Lógicos - resto da Divisão
print("Exponeciação", 2 ** 3)       # Seria (2 * 2 * 2 = 8)

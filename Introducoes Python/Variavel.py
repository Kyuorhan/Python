""" Tipos de Variavel """

"""
    Numérico Inteiro ---> Int
        Ex: Variável int 47
        
    Numérico Flutuante ---> Float
        Ex: Variável float 10.78
        
    Textual (String) ---> Str
        Ex: Varável str "Eu sou uma string"
        
    Booleana (True/False)
        Ex: Se é Veradeiro(True) ou Falso(False)
        
"""

"""     Variável Inteiro    """

var1 = 47  # Variável Inteira[int]
print("Variável Inteira:",var1)

"""     Variável Flutuantes     """

var2 = 10.78  # Variável Flutuante[float]
print("Varável Flutuante:",var2)

"""     Variável Textual     """

var3 = "Isso é uma String"   # Variável String[str]
print("Variável String:",var3)

"""     Booleana     """

var4 = True # True(Verdadeiro)
Var4 = False  # False(Falso)
print("Varável Booleana:",var4)
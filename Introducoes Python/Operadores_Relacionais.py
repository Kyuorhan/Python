""" Operadores Relacionais """

"""
    ==  <--->   Igual;
    !=  <--->   Diferente;
    >   <--->   Maior;
    <   <--->   Menor;
    >=  <--->   Maior ou Igual;
    <=  <--->   Menor ou Igual;
    
"""
x = 7
y = 5
""" Igual """
print("x é Igual y:", x == y)   #   x Igual y, Então True(Verdadeiro), se não False(Falso)
""" Diferente """
print("x é Diferente ou Igual y:", x != y)   #   x Diferente y, Então True, se não False
""" Maior """
print("x é Maior que y:", x > y)    #   x Maior y, Então True, se não False
""" Menor """
print("x é Menor que y:", x < y)    #   x Menor y, Então True, se não False
""" Maior ou Igual """
print("x é Maior ou Igual y:", x >= y)   #   x Maior ou Igual y, Então True, se não False
""" Menor ou Igual(Dvisão) """
print("x é Menor ou Igual y:", x <= y)   #   x Menor ou Igual y, Então True, se não False

soma = x + y
print("Se a soma é Diferente ou Igual x:", soma != x)                           # soma é Diferente ou Igual y, Então True, se não False
